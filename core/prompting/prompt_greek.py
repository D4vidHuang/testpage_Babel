    @staticmethod
    def assignment_evaluate_cluster(cluster_name: str, cluster_taxonomy: str) -> str:
        """Δημιουργία προτροπής εργασίας για αξιολόγηση ενός συγκεκριμένου συνόλου σφαλμάτων.
        
        Παράμετροι:
            cluster_name: Όνομα του συνόλου σφαλμάτων
            cluster_taxonomy: Φιλτραρισμένη ταξινομία για αυτό το σύνολο
        """
        cluster_descriptions = {
            "linguistic_grammar": "γλωσσική και γραμματική ορθότητα",
            "linguistic_language": "συνέπεια γλώσσας και καταλληλότητα",
            "semantic_accuracy": "σημασιολογική ακρίβεια και πληρότητα",
            "semantic_code": "ακρίβεια αποσπάσματος κώδικα",
            "model_behavior": "σφάλματα και αντικείμενα ειδικά του μοντέλου",
            "syntax_format": "μορφή και δομή σχολίου",
            "meta": "κριτήρια αποκλεισμού και διάφορα προβλήματα"
        }
        
        description = cluster_descriptions.get(cluster_name, cluster_name)
        
        return (
            f"Αξιολογείτε σχόλια κώδικα μόνο για {description.upper()}.\\n\\n"
            f"Σχετικές κατηγορίες σφαλμάτων για αυτήν την αξιολόγηση:\\n{cluster_taxonomy}\\n\\n"
            "ΣΗΜΑΝΤΙΚΟ: Εστιάστε ΜΟΝΟ στους τύπους σφαλμάτων που αναφέρονται παραπάνω. "
            "Αγνοήστε άλλα πιθανά ζητήματα—θα αξιολογηθούν ξεχωριστά.\\n\\n"
            "Για κάθε κατηγορία σφάλματος παραπάνω, προσδιορίστε εάν το σφάλμα υπάρχει με βάση "
            "τα κριτήρια συμπερίληψης. Ελέγξτε πάντα τα κριτήρια εξαίρεσης πριν την επιβεβαίωση."
        )

